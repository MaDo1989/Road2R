CREATE PROCEDURE [dbo].[sp_getTablesStracture]
(
    -- Add the parameters for the stored procedure here
        @Tables TableNameList READONLY

)
AS
BEGIN

    -- Insert statements for procedure here
    SELECT 
        TABLE_NAME,
        COLUMN_NAME,
        DATA_TYPE,
        IS_NULLABLE,
        CHARACTER_MAXIMUM_LENGTH
    FROM INFORMATION_SCHEMA.COLUMNS
    WHERE TABLE_NAME IN (SELECT TableName FROM @Tables) AND
	COLUMN_NAME IN (
	'RidePatNum',
	'PatientName',
	'PatientCellPhone',
	'PatientId',
	'PatientGender',
	'PatientStatus',
	'PatientBirthDate',
	'AmountOfEscorts',
	'AmountOfEquipments',
	'Destination',
	'Origin',
	'pickupTime',
	'Coordinator',
	'Remark',
	'Status',
	'Area',
	'OnlyEscort',
	'lastModified',
	'CoordinatorID',
	'MainDriver',
	'DriverName',
	'DriverCellPhone',
	'NoOfDocumentedRides',
	'IsAnonymous',
	'IsNewDriver',
	'Id',
	'DisplayName',
	'CellPhone',
	'CellPhone2',
	'Address',
	'JoinDate',
	'IsActive',
	'Gender',
	'Remarks',
	'CityCityName',
	'AvailableSeats',
	'VolunteerIdentity',
	'isDriving',
	'joinYear',
	'NoOfDocumentedCalls',
	'NoOfDocumentedRides',
	'LastUpdateBy',
	'No_of_Rides',
	'IsBooster',
	'IsBabyChair'
	)
    ORDER BY TABLE_NAME, ORDINAL_POSITION;
END



